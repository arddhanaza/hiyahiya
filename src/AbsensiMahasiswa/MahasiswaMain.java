    private void btAbsenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAbsenActionPerformed
        Absensi ab = new Absensi();
        MataKuliah matkul = new MataKuliah();
        matkul.get_jadwal_kuliah(ruang);
        
        mhs.cek_jam_kuliah(txNim.getText(), ruang);
        if(mhs.status_jam == true){
            ab.add_absensi(mhs.id_mahasiswa, mhs.id_dosen, ruang, mhs.id_matakuliah);
            if(ab.cek == true){
                new AbsensiMain().setVisible(rootPaneCheckingEnabled);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null,"Gagal Menyimpan");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Belum saatnya absen");
        }
        
    }//GEN-LAST:event_btAbsenActionPerformed

