    private void btCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCheckActionPerformed
        
        mhs.cek_data(txNim.getText());
        mhs.cek_matakuliah_mahasiswa(txNim.getText(), ruang);
        if(mhs.cek == true){
            if(mhs.tes == true ){
                String nim = txNim.getText();
                mhs.get_data_mahasiswa(nim);
                labelNama.setText(mhs.nama_mahasiswa);
                labelUsername.setText(mhs.nim_mahasiswa);
                labelKelas.setText(mhs.kelas_mahasiswa);
            }else{
                JOptionPane.showMessageDialog(null, "Mahasiswa tidak terdaftar matakuliah pada ruangan ini");
            }
        }else{
            JOptionPane.showMessageDialog(null, "DATA TIDAK ADA");
        }
        if(!labelNama.getText().isEmpty()){
            txNim.disable();
        }
        
    }//GEN-LAST:event_btCheckActionPerformed

